//=============================================================
// 初期化
//=============================================================
void Collision::Init()
{
	// すでにコリジョンコンポーネントが含まれているとき
	if (1 < static_cast<int>(gameObject->GetComponents<Collision>().size()))
	{
		Destroy(this);
	}
}

